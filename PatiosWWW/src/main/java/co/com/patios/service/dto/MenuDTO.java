package co.com.patios.service.dto;

public class MenuDTO {
	
	private Integer idMenu;
	private String nombreMenu;
	private String url;	
	private Integer idTipoMenu;
	/**
	 * @return the idMenu
	 */
	public Integer getIdMenu() {
		return idMenu;
	}
	/**
	 * @param idMenu the idMenu to set
	 */
	public void setIdMenu(Integer idMenu) {
		this.idMenu = idMenu;
	}
	/**
	 * @return the nombreMenu
	 */
	public String getNombreMenu() {
		return nombreMenu;
	}
	/**
	 * @param nombreMenu the nombreMenu to set
	 */
	public void setNombreMenu(String nombreMenu) {
		this.nombreMenu = nombreMenu;
	}
	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}
	/**
	 * @param url the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}
	/**
	 * @return the idTipoMenu
	 */
	public Integer getIdTipoMenu() {
		return idTipoMenu;
	}
	/**
	 * @param idTipoMenu the idTipoMenu to set
	 */
	public void setIdTipoMenu(Integer idTipoMenu) {
		this.idTipoMenu = idTipoMenu;
	}
	
	
	

}
