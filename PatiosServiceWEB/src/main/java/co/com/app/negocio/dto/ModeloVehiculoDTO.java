package co.com.app.negocio.dto;

public class ModeloVehiculoDTO {
	
	private Integer idModeloVehiculo;
	private Long anioModeloVehiculo;
	private String modeloVehiculo;
	/**
	 * @return the idModeloVehiculo
	 */
	public Integer getIdModeloVehiculo() {
		return idModeloVehiculo;
	}
	/**
	 * @param idModeloVehiculo the idModeloVehiculo to set
	 */
	public void setIdModeloVehiculo(Integer idModeloVehiculo) {
		this.idModeloVehiculo = idModeloVehiculo;
	}
	/**
	 * @return the anioModeloVehiculo
	 */
	public Long getAnioModeloVehiculo() {
		return anioModeloVehiculo;
	}
	/**
	 * @param anioModeloVehiculo the anioModeloVehiculo to set
	 */
	public void setAnioModeloVehiculo(Long anioModeloVehiculo) {
		this.anioModeloVehiculo = anioModeloVehiculo;
	}
	/**
	 * @return the modeloVehiculo
	 */
	public String getModeloVehiculo() {
		return modeloVehiculo;
	}
	/**
	 * @param modeloVehiculo the modeloVehiculo to set
	 */
	public void setModeloVehiculo(String modeloVehiculo) {
		this.modeloVehiculo = modeloVehiculo;
	}
	
	

}
